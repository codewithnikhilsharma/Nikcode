package Now;

	  
public class Example {
	  public static void main(String[] args) {
	    int a = 5;
	    int b = 10;
	    int sum = addNumbers(a, b);
	    System.out.println("The sum of " + a + " and " + b + " is " + sum);
	  }

	  public static int addNumbers(int x, int y) {
	    int result = x + y;
	    return result;
	  }
	}

